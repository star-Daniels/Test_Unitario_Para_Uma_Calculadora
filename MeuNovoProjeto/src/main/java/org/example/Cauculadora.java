package org.example;

public class Cauculadora {
    public double somar(double a, double b){
        return a+b;
    }

    public double diminuir(double a, double b){
        return a-b;
    }

    public double multiplicar(double a, double b){
        return a*b;
    }

    public double dividir(double a, double b){
        return a/b;
    }
}
